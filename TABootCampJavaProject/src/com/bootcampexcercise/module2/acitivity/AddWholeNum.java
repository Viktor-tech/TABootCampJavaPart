package com.bootcampexcercise.module2.acitivity;

public class AddWholeNum {

    public static void main(String[] args){
        int total =0;
        for (int i = 50; i <= 50; i++) {
            total = total + i;
        }
        System.out.println("Addition of whole numbers from 50 to 100 is "+ total);
    }

}
